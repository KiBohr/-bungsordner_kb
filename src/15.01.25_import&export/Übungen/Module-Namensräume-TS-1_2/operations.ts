//* vier Funktionen

//? 1. add
function addTwoNumbers(a: number, b: number): number {
  return a + b;
}

//? 2. substract
function substractTwoNumbers(a: number, b: number): number {
  return a - b;
}

//? 3. mutiply
function multiplyTwoNumbers(a: number, b: number): number {
  return a * b;
}

//? 4. divide
function divideTwoNumbers(a: number, b: number): number {
  return a / b;
}

export {
  addTwoNumbers,
  substractTwoNumbers,
  multiplyTwoNumbers,
  divideTwoNumbers,
};
